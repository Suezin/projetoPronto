    private void btnSelecionarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSelecionarMouseClicked
        if (tblAnimais.getSelectedRow() != -1) {
            //Pega a linha selecionada
            int linhaSelecionada = tblAnimais.getSelectedRow();

            String textoNome = tblAnimais.getValueAt(linhaSelecionada, 1).toString();
            nome = textoNome;
            int txtId = (int) tblAnimais.getValueAt(linhaSelecionada, 0);
            id = txtId;
            //Fecha a tela
            this.dispose();
        }
    }//GEN-LAST:event_btnSelecionarMouseClicked

    
     public void pesquisar(){
        DefaultTableModel modelotabela = (DefaultTableModel) tblAnimais.getModel();
        
        modelotabela.setNumRows(0);
         AnimalController controller = new AnimalController();
        List<Animal> listaAnimal = controller.consultar();
        
        for(Animal animal : listaAnimal) {
            Object[] linha = {
                animal.getId(),
                animal.getNome(),
                animal.getTipo(),
                animal.getRaca(),
            };
            
            modelotabela.addRow(linha);
        }
    }
