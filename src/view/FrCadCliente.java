     public void gravar() {
         Clientes c = new Clientes();
        c.setNome(txtNome.getText());
        c.setTelefone(txtTelefone.getText());
        c.setDataNasc(utils.Utils.converterStringToDate(txtDataNasc.getText()));

        ClienteController cl = new ClienteController();
        if (cl.inserir(c)) {
            JOptionPane.showMessageDialog(null, "Cliente inserido com sucesso!");
        } else {
            JOptionPane.showMessageDialog(null, "Erro ao inserir Cliente");
        }

    }
     
     public void pesquisar(){
        DefaultTableModel modelotabela = (DefaultTableModel) tblClientes.getModel();
        
        modelotabela.setNumRows(0);
        ClienteController controller = new ClienteController();
        List<Clientes> listaClientes = controller.consultar();
        
        for(Clientes c : listaClientes) {
            Object[] linha = {
                c.getPkCliente(),
                c.getNome(),
                c.getTelefone(),
                c.getDataNasc(),
            };
            
            modelotabela.addRow(linha);
        }
    }
